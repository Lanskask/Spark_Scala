
// String formating
printf("A str: %s, Int: %d, Float: %11.3f", "Hella", 1, 11.1)
// A str: Hella, Int: 1, Float:      11,100
printf("A str: %s, Int: %d, Float: %-11.3f", "Hella", 1, 11.1)
// A str: Hella, Int: 1, Float: 11,100
printf("A str: %s, Int: %d, Float: %+11.3f", "Hella", 1, 11.1)
// A str: Hella, Int: 1, Float:     +11,100
printf("A str: %s, Int: %d, Float: %11.30f", "Hella", 1, 11.1)
// A str: Hella, Int: 1, Float: 11,100000000000000000000000000000
printf("A str: %s, Int: %d, Float: %45.30f", "Hella", 1, 11.1)
// A str: Hella, Int: 1, Float:             11,100000000000000000000000000000
